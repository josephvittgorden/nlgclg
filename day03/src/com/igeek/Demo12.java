package com.igeek;

/**
 * @author zx
 * @version1.0
 * @description:遍历字符串(获取字符串中的每一个字符)
 */
public class Demo12 {

	public static void main(String[] args) {
		String str = "gagjyhuhkl";
		for (int i = 0; i < str.length(); i++) {
			System.out.print(str.charAt(i)+" ");
		}
		System.out.println();

	}

}
